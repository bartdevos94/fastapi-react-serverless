// src/components/ui/Input.tsx
import React, { forwardRef } from 'react'
import { cn } from '@/utils/helpers'
import { InputProps } from '@/types/common'

interface ExtendedInputProps extends React.InputHTMLAttributes<HTMLInputElement>, InputProps {}

const Input = forwardRef<HTMLInputElement, ExtendedInputProps>(
  ({ className, type = 'text', label, error, required, ...props }, ref) => {
    return (
      <div className="space-y-2">
        {label && (
          <label className="text-sm font-medium leading-none peer-disabled:cursor-not-allowed peer-disabled:opacity-70">
            {label}
            {required && <span className="text-red-500 ml-1">*</span>}
          </label>
        )}
        <input
          type={type}
          className={cn(
            'flex h-10 w-full rounded-md border border-gray-300 bg-white px-3 py-2 text-sm placeholder:text-gray-400 focus:outline-none focus:ring-2 focus:ring-primary-500 focus:border-transparent disabled:cursor-not-allowed disabled:opacity-50',
            error && 'border-red-500 focus:ring-red-500',
            className
          )}
          ref={ref}
          {...props}
        />
        {error && (
          <p className="text-sm text-red-600">{error}</p>
        )}
      </div>
    )
  }
)

Input.displayName = 'Input'
export { Input }